class Solution {
  /**
   * nums — массив чисел
   *
   * задача:
   * вернуть массив, где каждый элемент = сумма всех элементов до него включительно
   *
   * пример:
   * [1,2,3,4] → [1,3,6,10]
   *
   * используем prefix sum → O(n)
   */
  runningSum(nums) {
    // результирующий массив
    const result = [];

    // переменная для накопления суммы
    let runningTotal = 0;

    // проходим по каждому элементу массива
    for (let index = 0; index < nums.length; index++) {
      /**
       * добавляем текущее значение к накопленной сумме
       *
       * пример:
       * шаг 1: 0 + 1 = 1
       * шаг 2: 1 + 2 = 3
       * шаг 3: 3 + 3 = 6
       */
      runningTotal = runningTotal + nums[index];

      /**
       * сохраняем текущую сумму в результат
       *
       * result:
       * [1]
       * [1,3]
       * [1,3,6]
       */
      result.push(runningTotal);
    }

    // возвращаем итоговый массив
    return result;
  }
}
