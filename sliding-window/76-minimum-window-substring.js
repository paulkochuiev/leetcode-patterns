/*
========================================================
LeetCode: Minimum Window Substring
Сложность: Hard

Задача
--------------------------------------------------------
Даны две строки:

s — основная строка
t — строка с требуемыми символами

Нужно найти САМУЮ КОРОТКУЮ подстроку в s,
которая содержит ВСЕ символы из t (включая дубликаты).

Если такой подстроки нет — вернуть "".

Пример:

s = "OUZODYXAZV"
t = "XYZ"

Ответ: "YXAZ"

--------------------------------------------------------
Главная идея решения

Используем технику SLIDING WINDOW (скользящее окно).

1. Расширяем окно вправо (двигаем r)
2. Когда окно содержит все символы t
3. Начинаем сужать окно слева (двигаем l)
4. Сохраняем минимальное окно

Используем два объекта:

countT — сколько символов нужно
window — сколько символов сейчас в окне

Сложность:
Time: O(n)
Space: O(m)
========================================================
*/

class Solution {
  /**
   * @param {string} s
   * @param {string} t
   * @return {string}
   */
  minWindow(s, t) {
    // Если t пустая — ничего искать не нужно
    if (t === "") {
      return "";
    }

    // countT — сколько символов нам нужно
    // пример:
    // t = "AABC"
    // countT = { A:2, B:1, C:1 }
    let countT = {};

    // window — сколько символов сейчас внутри окна
    let window = {};

    // считаем частоту символов строки t
    for (let c of t) {
      countT[c] = (countT[c] || 0) + 1;
    }

    /*
    have — сколько символов уже удовлетворяют условию
    need — сколько всего уникальных символов нужно
    */
    let have = 0;

    let need = Object.keys(countT).length;

    /*
    res — хранит индексы лучшего окна
    пример: [startIndex, endIndex]
    */
    let res = [-1, -1];

    // длина минимального найденного окна
    let resLen = Infinity;

    // левый указатель окна
    let l = 0;

    /*
    начинаем расширять окно вправо
    */
    for (let r = 0; r < s.length; r++) {
      let c = s[r];

      // добавляем символ в текущее окно
      window[c] = (window[c] || 0) + 1;

      /*
      если этот символ нам нужен
      и количество в окне стало достаточным
      увеличиваем have
      */
      if (countT[c] && window[c] === countT[c]) {
        have++;
      }

      /*
      если have === need
      значит окно содержит ВСЕ нужные символы
      */
      while (have === need) {
        // проверяем меньше ли текущее окно
        if (r - l + 1 < resLen) {
          resLen = r - l + 1;
          res = [l, r];
        }

        /*
        пытаемся уменьшить окно слева
        */
        const leftChar = s[l];

        window[leftChar]--;

        /*
        если мы удалили важный символ
        и теперь его стало меньше чем нужно
        уменьшаем have
        */
        if (countT[leftChar] && window[leftChar] < countT[leftChar]) {
          have--;
        }

        // двигаем левый указатель
        l++;
      }
    }

    /*
    если мы так и не нашли окно
    */
    if (resLen === Infinity) {
      return "";
    }

    /*
    возвращаем подстроку
    */
    return s.slice(res[0], res[1] + 1);
  }
}
