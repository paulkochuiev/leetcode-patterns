/*
========================================================
LeetCode 424. Longest Repeating Character Replacement
Сложность: Medium
Паттерн: Sliding Window

Задача
--------------------------------------------------------
Дана строка s, состоящая из заглавных английских букв,
и число k.

Можно выполнить максимум k замен символов в строке.
Каждая замена позволяет заменить любой символ строки
на любой другой символ.

Нужно найти длину самой длинной подстроки,
которую можно превратить в строку,
состоящую из одинаковых символов,
сделав не более k замен.

--------------------------------------------------------
Пример 1

Input:
s = "ABAB", k = 2

Output:
4

Explanation:
Можно заменить два символа 'A' на 'B'
или два 'B' на 'A' → получим "AAAA" или "BBBB".
Самая длинная такая подстрока имеет длину 4.

--------------------------------------------------------
Пример 2

Input:
s = "AABABBA", k = 1

Output:
4

Explanation:
Можно заменить один символ:

"AABA" → заменить 'B' на 'A'
получим "AAAA"

Длина = 4.

--------------------------------------------------------
Идея решения (Sliding Window)

Мы двигаем окно [l, r] по строке.

windowSize = r - l + 1

maxf = частота самого частого символа в окне.

Сколько замен нужно сделать:
windowSize - maxf

Если это число > k
→ окно невалидно
→ двигаем левую границу l.

Если окно валидно
→ обновляем максимальный результат.

Сложность:
Time: O(n)
Space: O(1) (максимум 26 символов)
========================================================
*/

class Solution {
  /**
   * @param {string} s
   * @param {number} k
   * @return {number}
   */
  characterReplacement(s, k) {
    // Map хранит сколько раз каждый символ встречается в текущем окне
    // пример: { A:3, B:2 }
    let count = new Map();

    // максимальная длина валидного окна
    let res = 0;

    // l — левая граница sliding window
    // maxf — сколько раз встречается самый частый символ в окне
    let l = 0;
    let maxf = 0;

    // r — правая граница окна
    // двигаем окно вправо по строке
    for (let r = 0; r < s.length; r++) {
      // увеличиваем частоту текущего символа
      // если символа еще нет → начинаем с 1
      count.set(s[r], (count.get(s[r]) || 0) + 1);

      // обновляем максимальную частоту символа в окне
      // например если окно "AAB"
      // maxf = 2
      maxf = Math.max(maxf, count.get(s[r]));

      /**
       * проверяем валидность окна
       *
       * window size = r - l + 1
       *
       * сколько символов нужно заменить =
       * windowSize - mostFrequentCharacter
       *
       * если это число больше k
       * значит окно нельзя сделать одинаковым
       */
      while (r - l + 1 - maxf > k) {
        // уменьшаем частоту символа
        // который выходит из окна
        count.set(s[l], count.get(s[l]) - 1);

        // двигаем левую границу окна
        l++;
      }

      // если окно валидно
      // обновляем максимальную длину
      res = Math.max(res, r - l + 1);
    }

    // возвращаем максимальный размер окна
    return res;
  }
}
